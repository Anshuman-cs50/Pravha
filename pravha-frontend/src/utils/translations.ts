// Helper function to get translated text
export const getTranslatedText = (category: string, key: string, language: string = 'en'): string => {
  const translations = STATIC_TRANSLATIONS as any;
  if (translations[category] && translations[category][key]) {
    return translations[category][key][language] || translations[category][key]['en'];
  }
  return key; // Fallback to key if translation not found
};
